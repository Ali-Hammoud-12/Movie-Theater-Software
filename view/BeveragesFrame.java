    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        String total = tot2.getText();
        int result = JOptionPane.showConfirmDialog(null, "Your total for beverages is : " + total+"\n"+"Do you want to continue?");
        if (result == JOptionPane.YES_OPTION) {
            
            controller.addBeveragesPurchase(ocontroller.getCart2());
            controller.reducequantity(ocontroller.getCart2());
            number2.setValue(1);
            tot2.setText("");
            out2.setText("");
            counter2.setText("0");
            this.dispose();

        }
    }//GEN-LAST:event_jButton4ActionPerformed

