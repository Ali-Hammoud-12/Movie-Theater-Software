    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String choice = bevcombo.getSelectedItem().toString();
        int q = Integer.parseInt(totalquantity.getText());
        if (q <= 0 || number2.getValue().toString().compareTo(totalquantity.getText())>0) {

            JOptionPane.showMessageDialog(null, "The ordered amount exceeds the available stock.", "Error message", JOptionPane.WARNING_MESSAGE);

        } else {
            ocontroller.addtocart2(choice, Integer.parseInt(number2.getValue().toString()), LoginFrame.activeCustomer);
            out2.setText(ocontroller.printcart2());

            counter2.setText(ocontroller.countBeverages() + "");
            tot2.setText(ocontroller.estimateBeveragesprice() + "");

        }
    }//GEN-LAST:event_jButton1ActionPerformed

